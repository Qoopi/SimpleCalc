public class Math {

    double add(double a, double b) {
        return a + b;
    }

    double minus(double a, double b) {
        return a - b;
    }

    double multiply(double a, double b) {
        return a * b;
    }

    double divide(double a, double b) {
        return a / b;
    }

    double power(double a, double b) {
        return a % b;
    }
    double average (double a, double b) {
        return (a + b)/2;
    }

}